    @NotNull
    @Size(min = 3)
    @Column(name = "name", nullable = false)
    private String name;

    @NotNull
    @Size(min = 1, max = 100)
    @Column(name = "description", length = 100, nullable = false)
    private String description;

    @ManyToOne
    private User user;

